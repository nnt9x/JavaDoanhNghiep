public class Main {
    public static void main(String[] args) {
        // Khai bao bien a = 5
        int a = 5;
        // In ra bang
        System.out.println(a);
        // Khai bao bien b va c: so nguyen
        int b, c;
        b = 10;
        c = 20;
        int t = b + c;
        System.out.println("Tong: " + t);
        // Khai bao d la so thực (có thập phân 1.1, 1.2...)
        double d = 10.5;
        System.out.println(d);
    }
}
